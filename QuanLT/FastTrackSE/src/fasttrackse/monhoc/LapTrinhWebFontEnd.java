package fasttrackse.monhoc;

public class LapTrinhWebFontEnd {
	String tenMonHoc = "Lập trình web font-end";
	String giangVienPhuTrach = "Thầy 3";
	public LapTrinhWebFontEnd(String tenMonHoc, String giangVienPhuTrach) {
		super();
		this.tenMonHoc = tenMonHoc;
		this.giangVienPhuTrach = giangVienPhuTrach;
	}
	public String getTenMonHoc() {
		return tenMonHoc;
	}
	public void setTenMonHoc(String tenMonHoc) {
		this.tenMonHoc = tenMonHoc;
	}
	public String getGiangVienPhuTrach() {
		return giangVienPhuTrach;
	}
	public void setGiangVienPhuTrach(String giangVienPhuTrach) {
		this.giangVienPhuTrach = giangVienPhuTrach;
	}
}
